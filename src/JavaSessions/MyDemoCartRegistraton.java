package JavaSessions;

public class MyDemoCartRegistraton {
	String firstName;
	String lastName;
	String email;
	String telephone;
	String password;
	
	public MyDemoCartRegistraton(String firstName) {
		this.firstName = firstName;
		
	}
	public MyDemoCartRegistraton(String firstName, String lastName) {
		this.firstName = firstName;
		this.lastName = lastName;
		
	}
	public MyDemoCartRegistraton(String firstName , String lastName , String email) {
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		
	}
	public MyDemoCartRegistraton(String firstName , String lastName , String email, String telephone) {
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		this.telephone = telephone;
		
	}
	public MyDemoCartRegistraton(String firstName , String lastName , String email , String telephone, String password ) {
		this.firstName = firstName;
		this.lastName = lastName;
		this.email = email;
		this.telephone = telephone;
		this.password = password;
		
	}
	
	
	
}
